USE dispute_resolve;

-- 调解记录模板表
CREATE TABLE IF NOT EXISTS mediation_record_template (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
    template_name VARCHAR(128) NOT NULL COMMENT '模板名称',
    template_code VARCHAR(64) NOT NULL UNIQUE COMMENT '模板编码',
    category VARCHAR(64) DEFAULT '' COMMENT '模板分类: neighborhood-邻里纠纷 wage-欠薪纠纷 property-物业纠纷 contract-合同纠纷 family-家庭纠纷 traffic-交通事故 consumer-消费纠纷 other-其他',
    dispute_type_ids VARCHAR(512) DEFAULT '' COMMENT '适用纠纷类型ID列表(逗号分隔)',
    record_type TINYINT DEFAULT 1 COMMENT '默认记录类型: 1-初次调解 2-再次调解 3-补充调解',
    mediation_place VARCHAR(256) DEFAULT '' COMMENT '默认调解地点',
    process_content_template TEXT COMMENT '调解过程模板',
    dispute_focus_template TEXT COMMENT '争议焦点模板',
    mediation_opinion_template TEXT COMMENT '调解意见模板',
    agreement_content_template TEXT COMMENT '协议内容模板',
    next_step_template VARCHAR(512) DEFAULT '' COMMENT '下一步计划模板',
    default_duration INT DEFAULT 30 COMMENT '默认调解时长(分钟)',
    participants_template VARCHAR(512) DEFAULT '' COMMENT '参与方说明模板',
    tips TEXT COMMENT '填写提示/注意事项',
    is_system TINYINT DEFAULT 0 COMMENT '是否系统内置: 0-否 1-是',
    use_count INT DEFAULT 0 COMMENT '使用次数',
    sort_order INT DEFAULT 0 COMMENT '排序',
    status TINYINT DEFAULT 1 COMMENT '状态: 0-禁用 1-启用',
    creator_id BIGINT DEFAULT 0 COMMENT '创建人ID',
    creator_name VARCHAR(64) DEFAULT '' COMMENT '创建人姓名',
    org_id BIGINT DEFAULT 0 COMMENT '所属机构ID',
    org_name VARCHAR(128) DEFAULT '' COMMENT '所属机构名称',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted_at DATETIME DEFAULT NULL COMMENT '软删除时间',
    INDEX idx_category(category),
    INDEX idx_status(status),
    INDEX idx_is_system(is_system),
    INDEX idx_org_id(org_id),
    INDEX idx_deleted_at(deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='调解记录模板表';

